List swaras = ['S', 'R', 'G', 'M', 'P', 'D', 'N'];

String varjyaRaga(String thisString) {
  thisString = thisString
      .replaceAll('1', '')
      .replaceAll('2', '')
      .replaceAll('3', '')
      .replaceAll('↑', '')
      .replaceAll('↓', '');
  List thisStringList = thisString.split(' ');
  thisStringList.removeWhere((element) => element == '');
  var classificationNum = 0;
  for (int i = 0; i < 7; i++) {
    if (thisStringList.contains(swaras[i])) {
      classificationNum += 1;
    }
  }
  if (classificationNum == 7) {
    return 'sampoorna';
  } else if (classificationNum == 6) {
    return 'sHADava';
  } else if (classificationNum == 5) {
    return 'OuDava';
  } else if (classificationNum == 4) {
    return 'swarAntara';
  } else {
    return '';
  }
}

bool subSet(List a, List b) {
  for (int i = 0; i < a.length; i++) {
    if (b.contains(a[i])) {
      continue;
    } else {
      return false;
    }
  }
  return true;
}

String upangaBhashanga(String raga) {
  var janyaPosition = ragaName.indexOf(raga) - 72;
  var janakaPosition = janyaRagaJanakaNumber[janyaPosition] - 1;
  String janakaArohana = arohanaJanaka[janakaPosition];
  String janyaArohana = arohanaJanya[janyaPosition];
  String janyaAvarohana = avarohanaJanya[janyaPosition];
  janakaArohana = janakaArohana.replaceAll('↑', '');
  janyaArohana = janyaArohana.replaceAll('↑', '').replaceAll('↓', '');
  janyaAvarohana = janyaAvarohana.replaceAll('↑', '').replaceAll('↓', '');
  List janakaArohanaList = janakaArohana.split(' ');
  List janyaArohanaList = janyaArohana.split(' ');
  List janyaAvarohanaList = janyaAvarohana.split(' ');
  janakaArohanaList.removeWhere((element) => element == '');
  janyaArohanaList.removeWhere((element) => element == '');
  janyaAvarohanaList.removeWhere((element) => element == '');
  if (subSet(janyaArohanaList, janakaArohanaList) &&
      subSet(janyaAvarohanaList, janakaArohanaList)) {
    return 'upAnga rAga';
  } else {
    return 'BhAShAnga rAga';
  }
}

String vakra(String arohana, String avarohana) {
  arohana = arohana
      .replaceAll('↑', '')
      .replaceAll('↓', '')
      .replaceAll('S', '')
      .replaceAll('1', '')
      .replaceAll('2', '')
      .replaceAll('3', '');
  avarohana = avarohana
      .replaceAll('↑', '')
      .replaceAll('↓', '')
      .replaceAll('S', '')
      .replaceAll('1', '')
      .replaceAll('2', '')
      .replaceAll('3', '');
  List arohanaList = arohana.split(' ');
  List avarohanaList = avarohana.split(' ');
  arohanaList.removeWhere((element) => element == '');
  avarohanaList.removeWhere((element) => element == '');
  for (int i = 1; i < arohanaList.length; i++) {
    if (swaras.indexOf(arohanaList[i - 1]) < swaras.indexOf(arohanaList[i])) {
      continue;
    } else {
      return 'Vakra rAga';
    }
  }
  for (int i = 1; i < avarohanaList.length; i++) {
    if (swaras.indexOf(avarohanaList[i - 1]) >
        swaras.indexOf(avarohanaList[i])) {
      continue;
    } else {
      return 'Vakra rAga';
    }
  }
  return '';
}
